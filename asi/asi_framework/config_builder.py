from .config import (
    DEFAULT_CORE_MODEL,
    DEFAULT_CORE_TYPE,
    DEFAULT_FREQUENCY,
    DEFAULT_LOGICAL_CPUS,
    DEFAULT_L1I_SIZE,
    DEFAULT_L1D_SIZE,
    DEFAULT_L2_SIZE,
    DEFAULT_L3_SIZE,
    DEFAULT_L1I_ASSOC,
    DEFAULT_L1D_ASSOC,
    DEFAULT_L2_ASSOC,
    DEFAULT_L3_ASSOC,
    DEFAULT_BRANCH_PREDICTOR_TYPE,
    DEFAULT_BRANCH_MISPREDICT_PENALTY,
    DEFAULT_BRANCH_PREDICTOR_SIZE,
    DEFAULT_ROB_RS_ENTRIES,
    DEFAULT_ROB_OUTSTANDING_LOADS,
    DEFAULT_ROB_OUTSTANDING_STORES,
    DEFAULT_ROB_COMMIT_WIDTH,
    DEFAULT_ROB_IN_ORDER,
    DEFAULT_ROB_STORE_TO_LOAD_FORWARDING,
    DEFAULT_ROB_ADDRESS_DISAMBIGUATION,
)
 
 
def build_runtime_config(
    reference_config: str,
    *,
    cores: int = None,
    core_model: str = None,
    core_type: str = None,
    frequency: float = None,
    logical_cpus: int = None,
    l1i_size: int = None,
    l1i_assoc: int = None,
    l1d_size: int = None,
    l1d_assoc: int = None,
    l2_size: int = None,
    l2_assoc: int = None,
    l3_size: int = None,
    l3_assoc: int = None,
    branch_predictor_type: str = None,
    branch_mispredict_penalty: int = None,
    branch_predictor_size: int = None,
    rob_rs_entries: int = None,
    rob_outstanding_loads: int = None,
    rob_outstanding_stores: int = None,
    rob_commit_width: int = None,
    rob_in_order: str = None,
    rob_store_to_load_forwarding: str = None,
    rob_address_disambiguation: str = None,
 
) -> str:
        resolved_core_type = core_type or DEFAULT_CORE_TYPE
 
        lines = [
            "# Auto-generated by framework/asi.py",
            "#include base",
            "#include gainestown",
            "#include rob",
            f"# Reference config: {reference_config}",
            "",
            "[general]",
            f"total_cores = {cores or 1}",
            "",
            "[perf_model/core]",
            f"core_model = {core_model or DEFAULT_CORE_MODEL}",
            f"type = {resolved_core_type}",
            f"frequency = {frequency or DEFAULT_FREQUENCY}",
            f"logical_cpus = {logical_cpus or DEFAULT_LOGICAL_CPUS}",
            "",
            "[perf_model/l1_icache]",
            f"cache_size = {l1i_size or DEFAULT_L1I_SIZE}",
            f"associativity = {l1i_assoc or DEFAULT_L1I_ASSOC}",
            "",
            "[perf_model/l1_dcache]",
            f"cache_size = {l1d_size or DEFAULT_L1D_SIZE}",
            f"associativity = {l1d_assoc or DEFAULT_L1D_ASSOC}",
            "",
            "[perf_model/l2_cache]",
            f"cache_size = {l2_size or DEFAULT_L2_SIZE}",
            f"associativity = {l2_assoc or DEFAULT_L2_ASSOC}",
            "",
            "[perf_model/l3_cache]",
            f"cache_size = {l3_size or DEFAULT_L3_SIZE}",
            f"associativity = {l3_assoc or DEFAULT_L3_ASSOC}",
            "",
            "[perf_model/branch_predictor]",
            f"type = {branch_predictor_type or DEFAULT_BRANCH_PREDICTOR_TYPE}",
            f"mispredict_penalty = {branch_mispredict_penalty or DEFAULT_BRANCH_MISPREDICT_PENALTY}",
            f"size = {branch_predictor_size or DEFAULT_BRANCH_PREDICTOR_SIZE}",
            "",
        ]
    
        # Only meaningful for out-of-order cores. Sniper ignores this section
        # for in-order core types, but we skip emitting it to keep configs clean.
        if resolved_core_type == "rob":
            lines.extend([
                "[perf_model/core/rob_timer]",
                f"rs_entries = {rob_rs_entries or DEFAULT_ROB_RS_ENTRIES}",
                f"outstanding_loads = {rob_outstanding_loads or DEFAULT_ROB_OUTSTANDING_LOADS}",
                f"outstanding_stores = {rob_outstanding_stores or DEFAULT_ROB_OUTSTANDING_STORES}",
                f"commit_width = {rob_commit_width or DEFAULT_ROB_COMMIT_WIDTH}",
                f"in_order = {rob_in_order or DEFAULT_ROB_IN_ORDER}",
                f"store_to_load_forwarding = {rob_store_to_load_forwarding or DEFAULT_ROB_STORE_TO_LOAD_FORWARDING}",
                f"address_disambiguation = {rob_address_disambiguation or DEFAULT_ROB_ADDRESS_DISAMBIGUATION}",
                "",
            ])
    
        return "\n".join(lines)
 
